static void
proto_error(char* iob, int level, char* msg, char* arg)
{
	register char*	p;
	char		buf[1024];

	p = stpcpy(buf, "proto: ");
	if (iob)
	{
		register Proto_t*	proto = (Proto_t*)(iob - sizeof(Proto_t));

		if (proto->line)
		{
			if (proto->file)
			{
				*p++ = '"';
				p = stpcpy(p, proto->file);
				*p++ = '"';
				*p++ = ',';
				*p++ = ' ';
			}
			p = stpcpy(p, "line ");
			p = number(p, proto->line);
		}
		else if (proto->file)
			p = stpcpy(p, proto->file);
	}
	else
	{
		p = stpcpy(p, msg);
		msg = arg;
		arg = 0;
	}
	if (*(p - 1) != ' ')
	{
		*p++ = ':';
		*p++ = ' ';
	}
	if (level == 1)
		p = stpcpy(p, "warning: ");
	p = stpcpy(p, msg);
	if (arg)
	{
		*p++ = ' ';
		p = stpcpy(p, arg);
	}
	*p++ = '\n';
	write(2, buf, p - buf);
	if (level >= 3)
		exit(level - 2);
	if (level >= 2)
		errors++;
}

