#include "sfhdr.h"  // IWYU pragma: keep
#include "sfio.h"

#undef _sfgetu2

long _sfgetu2(Sfio_t *f, long v) {
    if (v < 0) return -1;
    sfungetc(f, v);
    return sfgetu(f);
}
