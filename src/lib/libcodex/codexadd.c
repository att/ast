#pragma prototyped

/*
 * add new codex method
 */

#include <codex.h>

int
codexadd(const char* name, Codexmeth_t* meth)
{
	if (meth->next)
		return -1;
	meth->next = codexstate.first;
	codexstate.first = meth;
	return 0;
}
